
public class ConjugatedVerb {
	String conjugatedVerb;
	int number;
	int person;
	int tense;
	
	public ConjugatedVerb(String conjugatedVerb, int tense, int number, int person){
		this.conjugatedVerb = conjugatedVerb;
		this.number = number;
		this.person = person;
		this.tense = tense;
	}
}
